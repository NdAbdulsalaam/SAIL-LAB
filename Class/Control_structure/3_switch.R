# Switch
# Switch is similar to if-else statement but more appropriate forn use when the alternatives are multiples.
# It takes a single input argument and executes a particular code based on the value of the input (called a case)

city <- "Abuja"

switch(city,
       "Lusaka" = "Zambia",
       "Nairobi" = "Kenya",
       "Bamako" = "Mali",
       "Abuja" = "Nigeria",
       "Cairo" = "Egypt",
       "Kampala" = "Kwara State",
       "Kigali" = "Rwanda",
       "Windhoek" = "Namibia",
       "Tunis" = "Tunisia",
       "Kingston" = "Jamaica",
       "Pretoria" = "South Africa"
)

